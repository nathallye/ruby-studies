# Um bloco pode ser entendido como uma função anônima, ou seja, uma função sem nome. 

# É definido entre **do**..**end** ou **colchetes** e da mesma forma que os métodos que foram vistos até agora, pode receber parâmetros para sua execução.

# Usamos **colchetes **para escrever blocos que ocupam apenas uma linha.
5.times { puts "Exec the block" } # 5 vezes é executado esse bloco de código
